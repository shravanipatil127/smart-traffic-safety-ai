"""
download_dataset.py — Downloads helmet & seatbelt datasets from Roboflow

USAGE:
  1. Create a free account at roboflow.com
  2. Get your API key from Settings > API
  3. Run: python download_dataset.py --api-key YOUR_KEY

Alternatively, manually download from:
  - Helmet: https://universe.roboflow.com/search?q=helmet+detection
  - Seatbelt: https://universe.roboflow.com/search?q=seatbelt+detection
  - Export as "YOLOv8" format into the dataset/ folder
"""

import os
import argparse


def download_roboflow(api_key, workspace, project, version, output_dir="dataset"):
    """Download a Roboflow dataset."""
    try:
        from roboflow import Roboflow
    except ImportError:
        print("[ERROR] roboflow package not installed.")
        print("  Run: pip install roboflow")
        return

    rf = Roboflow(api_key=api_key)
    proj = rf.workspace(workspace).project(project)
    dataset = proj.version(version).download("yolov8", location=output_dir)
    print(f"[OK] Dataset downloaded to: {dataset.location}")
    return dataset.location


def create_sample_yaml(output_dir="dataset"):
    """
    Creates a sample data.yaml so you can test training structure
    even before downloading a real dataset.
    """
    os.makedirs(f"{output_dir}/train/images", exist_ok=True)
    os.makedirs(f"{output_dir}/train/labels", exist_ok=True)
    os.makedirs(f"{output_dir}/valid/images", exist_ok=True)
    os.makedirs(f"{output_dir}/valid/labels", exist_ok=True)

    yaml_content = f"""# Helmet & Seatbelt Detection Dataset
# Generated by download_dataset.py

path: {os.path.abspath(output_dir)}
train: train/images
val: valid/images

nc: 4
names:
  0: helmet
  1: no_helmet
  2: seatbelt
  3: no_seatbelt
"""
    yaml_path = os.path.join(output_dir, "data.yaml")
    with open(yaml_path, "w") as f:
        f.write(yaml_content)

    print(f"[OK] Sample data.yaml created at: {yaml_path}")
    print("     Add your images to dataset/train/images/ and dataset/valid/images/")
    print("     Add YOLO-format labels to the corresponding labels/ folders")
    return yaml_path


if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--api-key", help="Your Roboflow API key")
    parser.add_argument("--workspace", default="", help="Roboflow workspace slug")
    parser.add_argument("--project",   default="helmet-detection", help="Project slug")
    parser.add_argument("--version",   type=int, default=1, help="Dataset version number")
    parser.add_argument("--sample",    action="store_true",
                        help="Just create sample folder structure + data.yaml")
    args = parser.parse_args()

    if args.sample or not args.api_key:
        print("[INFO] Creating sample dataset structure...")
        create_sample_yaml()
    else:
        download_roboflow(args.api_key, args.workspace, args.project, args.version)
